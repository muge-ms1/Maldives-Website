var jobSet = 1;

//Once DOM content is loaded Load all job cards
document.addEventListener("DOMContentLoaded", populateJobs);


/**
 * Function to capitalize First Letter of word/sentence
 * @param {String} string 
 * @returns String After capitalizing first letter
 */
function capitalizeFirstLetter(string) {
    return string.charAt(0).toUpperCase() + string.slice(1);
}

/**
 * Populate Job cards as sets of 8
 */
function populateJobs(){
    var cardsFrag = document.createDocumentFragment();

    for(let i=0; i<jobCircularList.length && i<(jobSet*8); i++){

        //Hide Load more when all Jobs are loaded
        if( jobCircularList.length < jobSet*8){
            $('.load-more').hide();
        }
        let card = document.createElement('div');
        card.classList.add('job-card');

        let cardHeader = document.createElement('div');
        cardHeader.classList.add('job-card-header');

        let cardImage = document.createElement('div');
        cardImage.classList.add('job-card-img');

        let imgElement = document.createElement('img');
        imgElement.src = jobCircularList[i].company_image;
        imgElement.alt = `${jobCircularList[i].company}'s Image`;
        cardImage.appendChild(imgElement);


        let companyDiv = document.createElement('div');
        companyDiv.classList.add('company');
        let companyName = document.createElement('div');
        companyName.classList.add('job-card-company-name');
        companyName.textContent = capitalizeFirstLetter(jobCircularList[i].company);
        companyDiv.append(companyName);
        let JobPlace = document.createElement('div');
        JobPlace.classList.add('job-card-place');
        JobPlace.textContent = capitalizeFirstLetter(jobCircularList[i].place);
        companyDiv.append(JobPlace);

        cardHeader.append(cardImage);
        cardHeader.append(companyDiv);

        let titleElement = document.createElement('div');
        titleElement.classList.add('job-card-title');
        titleElement.textContent = capitalizeFirstLetter(jobCircularList[i].title);

        let jobType = document.createElement('div');
        jobType.classList.add('job-card-type');
        jobType.textContent = capitalizeFirstLetter(jobCircularList[i].job_type);

        let jobDesc = document.createElement('div');
        jobDesc.classList.add('job-card-desc');
        jobDesc.textContent = capitalizeFirstLetter(jobCircularList[i].job_desc);

        let cardFooter = document.createElement('div');
        cardFooter.classList.add('job-card-footer');

        let cardSalary = document.createElement('div');
        cardSalary.classList.add('job-card-salary');
        let currency = document.createElement('span');
        currency.classList.add('job-card-salary-currency');
        currency.textContent = `${jobCircularList[i].salary_currency} `;
        cardSalary.append(currency);
        let amt = document.createElement('span');
        amt.classList.add('job-card-salary-amount');
        amt.textContent = jobCircularList[i].salary_amount;
        cardSalary.append(amt);
        let term = document.createElement('span');
        term.classList.add('job-card-salary-term');
        term.textContent = `/${jobCircularList[i].salary_term}`;
        cardSalary.append(term);
        cardFooter.append(cardSalary);

        let footerBtn = document.createElement('button');
        footerBtn.textContent = 'Apply Now';
        //btn functionality
        footerBtn.onclick = ()=>{
            window.location.href = `/pages/jobForm.html`;
            //window.location.pathname = `${window.location.pathname}/pages/jobForm.html`;
        }
        
        cardFooter.append(footerBtn);

        card.append(cardHeader);
        card.append(titleElement);
        card.append(jobType);
        card.append(jobDesc);
        card.append(cardFooter);

        cardsFrag.append(card);
    }

    document.getElementsByClassName('job-cards')[0].appendChild(cardsFrag);
}

/**
 * Load next X8 Job cards
 */
function loadMoreJobs(){
    //Increased no of cards shown (+8 cards)
    jobSet = jobSet + 1;
    //Remove all cards
    document.querySelectorAll('.job-card').forEach(card=>{
        card.remove();
    });
    //Re-populate job cards
    populateJobs();
}


// ---------- Login and Register Modal :START----------
var button;

/**
 * To open modal
 * @param {class name} modal 
 */
var openForm = function(modal) {
    if(modal == 'loginModal'){
        button = document.getElementsByClassName('mainButton')[0];
    }else{
        button = document.getElementsByClassName('mainButton')[1];
    }
	button.classList.add('active');
};

/**
 * Apply style after checking whether input field has input or not
 * @param {element} input 
 */
var checkInput = function(input) { 
	if (input.value.length > 0) {
		input.classList.add('active');
	} else {
		input.classList.remove('active');
	}
};

// Close Modal
var closeForm = function() {
    $('.input-group > input').val('');
	button.classList.remove('active');
};

//Escape key to close form
document.addEventListener("keyup", function(e) {
    // keycode deprecated <->27
	if (e.key == 'Escape') {
		closeForm();
	}
});
// ---------- Login and Register Modal :END----------


